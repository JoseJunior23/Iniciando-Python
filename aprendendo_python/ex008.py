# leia o valor em metros e converter para centimetros e milimitros

i = ('  Inicio Do Programa  ')
print('{:#^200}'.format(i))


v = float(input('Digite um valor em metros: '))
m = v * 100
cen = v * 1000
print('O valor digitado foi de {}, em centimetros é de {}, e em milimitros é de {}'.format(v,m,cen))


f = ('  Fim Do Programa  ')
print('{:#^200}'.format(f))